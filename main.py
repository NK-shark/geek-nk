print("Вычисления дохода по вкладу.")
print("Введите исходные данные:")
a=float(input("Величина вклада (руб.) -> "))
b=float(input("Срок вклада (дней)-> "))
c=float(input("Процентная ставка (годовых)-> "))
print("----------------------------")
d=(a/100*((b*c)/365))
print("Доход: ","{:0.2f}".format(d))
d=d+a
print("Сумма по окончании срока вклада: ","{:0.2f}".format(d))
